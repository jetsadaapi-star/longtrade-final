<?php

namespace App\Policies;

use App\Models\User;
use App\Models\ExpertAdvisor;
use Illuminate\Auth\Access\HandlesAuthorization;

class ExpertAdvisorPolicy
{
    use HandlesAuthorization;

    /**
     * Perform pre-authorization checks.
     */
    public function before(User $user, string $ability): ?bool
    {
        if ($user->hasRole('super_admin')) {
            return true;
        }
        return null;
    }

    /**
     * Determine whether the user can view any models.
     */
    public function viewAny(User $user): bool
    {
        return $user->can('view_any_expert::advisor');
    }

    /**
     * Determine whether the user can view the model.
     */
    public function view(User $user, ExpertAdvisor $expertAdvisor): bool
    {
        return $user->can('view_expert::advisor');
    }

    /**
     * Determine whether the user can create models.
     */
    public function create(User $user): bool
    {
        return $user->can('create_expert::advisor');
    }

    /**
     * Determine whether the user can update the model.
     */
    public function update(User $user, ExpertAdvisor $expertAdvisor): bool
    {
        return $user->can('update_expert::advisor');
    }

    /**
     * Determine whether the user can delete the model.
     */
    public function delete(User $user, ExpertAdvisor $expertAdvisor): bool
    {
        return $user->can('delete_expert::advisor');
    }

    /**
     * Determine whether the user can bulk delete.
     */
    public function deleteAny(User $user): bool
    {
        return $user->can('delete_any_expert::advisor');
    }

    /**
     * Determine whether the user can permanently delete.
     */
    public function forceDelete(User $user, ExpertAdvisor $expertAdvisor): bool
    {
        return $user->can('force_delete_expert::advisor');
    }

    /**
     * Determine whether the user can permanently bulk delete.
     */
    public function forceDeleteAny(User $user): bool
    {
        return $user->can('force_delete_any_expert::advisor');
    }

    /**
     * Determine whether the user can restore.
     */
    public function restore(User $user, ExpertAdvisor $expertAdvisor): bool
    {
        return $user->can('restore_expert::advisor');
    }

    /**
     * Determine whether the user can bulk restore.
     */
    public function restoreAny(User $user): bool
    {
        return $user->can('restore_any_expert::advisor');
    }

    /**
     * Determine whether the user can replicate.
     */
    public function replicate(User $user, ExpertAdvisor $expertAdvisor): bool
    {
        return $user->can('replicate_expert::advisor');
    }

    /**
     * Determine whether the user can reorder.
     */
    public function reorder(User $user): bool
    {
        return $user->can('reorder_expert::advisor');
    }
}
